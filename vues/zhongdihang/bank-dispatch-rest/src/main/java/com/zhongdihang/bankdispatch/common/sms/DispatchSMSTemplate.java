public class DispatchSMSTemplate implements Serializable {

    private static final long serialVersionUID = 5130916521221422618L;

    private String bankName;

    private String dispatchNo;

    private String contact;

    private String contactPhone;

    public DispatchSMSTemplate() {
    }

    public DispatchSMSTemplate(String bankName, String dispatchNo, String contact, String contactPhone) {
        this.bankName = bankName;
        this.dispatchNo = dispatchNo;
        this.contact = contact;
        this.contactPhone = contactPhone;
    }

    public String getBankName() {
        return bankName;
    }

    public void setBankName(String bankName) {
        this.bankName = bankName;
    }

    public String getContact() {
        return contact;
    }

    public void setContact(String contact) {
        this.contact = contact;
    }

    public String getContactPhone() {
        return contactPhone;
    }

    public void setContactPhone(String contactPhone) {
        this.contactPhone = contactPhone;
    }

    public String getDispatchNo() {
        return dispatchNo;
    }

    public void setDispatchNo(String dispatchNo) {
        this.dispatchNo = dispatchNo;
    }
}
